using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace XtremeEngineXNA.Gui
{
    /// <summary>
    /// Enum whose values decribe the different alignment options allowed for vertical container widgets.
    /// </summary>
    public enum VAlignMode
    {
        /// <summary>
        /// Aligns children to the top.
        /// </summary>
        TOP,
        /// <summary>
        /// Aligns children to the middle.
        /// </summary>
        MIDDLE,
        /// <summary>
        /// Aligns children to the bottom.
        /// </summary>
        BOTTOM
    };
}
