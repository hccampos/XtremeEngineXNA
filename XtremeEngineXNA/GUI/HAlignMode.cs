using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace XtremeEngineXNA.Gui
{
    /// <summary>
    /// Enum whose values decribe the different alignment options allowed for horizontal container widgets.
    /// </summary>
    public enum HAlignMode
    {
        /// <summary>
        /// Aligns children to the left.
        /// </summary>
        LEFT,
        /// <summary>
        /// Aligns children to the center.
        /// </summary>
        CENTER,
        /// <summary>
        /// Aligns children to the right.
        /// </summary>
        RIGHT
    };
}
